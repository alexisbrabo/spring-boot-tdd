CREATE SEQUENCE phone_id_seq as BIGINT START WITH 1 INCREMENTS BY 1;
CREATE TABLE phone (
	code BIGINT GENERATED BY DEFAULT as SEQUENCE phone_id_seq PRIMARY KEY,
	ddd VARCHAR(2) NOT NULL,
	number VARCHAR(9) NOT NULL,
	code_person BIGINT NOT NULL,
	FOREIGN KEY (code_person) REFERENCES person(code)
);